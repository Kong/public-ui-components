export const lookupStatusCodeColor = (label: string) => {
  const code = parseInt(label, 10)
  const codeClass = Math.floor(code / 100) * 100
  const color = statusCodeColors[code] ?? unknownStatusCodeColors[codeClass]

  return color ?? KUI_COLOR_BACKGROUND_DISABLED
}

const statusCodes = Array.from({ length: 500 }, (_, index) => 100 + index)

export const defaultStatusCodeColors: AnalyticsChartColors = {
  ...statusCodes.reduce((acc, statusCode) => {
    acc[`${statusCode}`] = lookupStatusCodeColor(`${statusCode}`)

    return acc
  }, {} as AnalyticsChartColors),
  ____OTHER____: '#DAD4C7',
  ...statusCodeGroupColors,
}

const EMPTY_COLOR = '#afb7c5'

export const determineBaseColor = (i: number, dimensionName: string, isEmpty: boolean, colorPalette: string[] | AnalyticsChartColors): string => {
  let baseColor

  if (isEmpty) {
    baseColor = EMPTY_COLOR
  } else if (Array.isArray(colorPalette)) {
    baseColor = lookupDatavisColor(i, colorPalette)
  } else {
    baseColor = colorPalette[dimensionName]
  }

  return baseColor || lookupDatavisColor(i) // fallback to default datavis palette if no color found
}

export const thresholdColor = (type: ThresholdType): string => {
  switch (type) {
    case 'error':
      return KUI_COLOR_TEXT_DANGER
    case 'warning':
      return KUI_COLOR_TEXT_WARNING_WEAK
    case 'neutral':
      return KUI_COLOR_BACKGROUND_NEUTRAL
    default:
      return KUI_COLOR_BACKGROUND_NEUTRAL
  }
}
