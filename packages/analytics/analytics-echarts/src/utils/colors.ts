  '1XX': KUI_STATUS_COLOR_100S,
  '2XX': KUI_STATUS_COLOR_200S,
  '3XX': KUI_STATUS_COLOR_300S,
  '4XX': KUI_STATUS_COLOR_400S,
  '5XX': KUI_STATUS_COLOR_500S,
